package i.ogeyingbo.walletaccount.model;

import com.fasterxml.jackson.annotation.JsonAutoDetect;
import com.fasterxml.jackson.annotation.PropertyAccessor;
import com.fasterxml.jackson.databind.ObjectMapper;
import java.util.ArrayList;
import org.json.JSONArray;

/**
 *
 * @author BOLAJI-OGEYINGBO
 */
public class TrxnHistoryModels  extends  ArrayList<TrxnHistoryModel> {
    
        public  final  JSONArray  convertToJSONArray(){
        JSONArray  returnedJson =  null;
        ObjectMapper objectMapper = new ObjectMapper();
        try{
             objectMapper.setVisibility(PropertyAccessor.FIELD, JsonAutoDetect.Visibility.ANY);
           returnedJson =  new JSONArray(objectMapper.writeValueAsString(this));
        }catch(Exception ex){
            ex.printStackTrace();
        }finally{
            objectMapper = null;
        }
      return  returnedJson;
    }
    
    
   
    
    
}
